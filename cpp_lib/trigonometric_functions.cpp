// 余弦定理
double cos_narrow_angle(double const &a, double const &b, double const &c)
{
    return (a * a + b * b - c * c) / (2 * a * b);
}

// 正弦定理
double circumradius(double const &a, double const &cos_narrow_angle)
{
    return a / (2 * sqrt(1 - pow(cos_narrow_angle, 2)));
}

